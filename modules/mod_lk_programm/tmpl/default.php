						<p>В условиях нехватки бюджетных инвестиций и потребности государства в реализации большого количества транспортных проектов механизм государственно-частного партнерства является наиболее востребованным.</p>

						<p>Транспортные проекты ГЧП московского региона включают в себя как масштабные  проекты, такие как развитие МАУ, создание ЛРТ, строительство ЦКАД и др., так и проекты значительно меньшей стоимости &ndash; строительство путепроводов, региональных автомобильных дорог, парковок, пешеходных переходов над железнодорожными путями и дорогами. И если в части громоздких проектов согласование существенных условий и параметров проекта происходит на федеральном уровне, то согласование ключевых вопросов региональных проектов происходит на стыке интересов инвесторов и публичной стороны.</p>

						<p>С появлением формы частной концессионной инициативы инвесторы получили возможность самостоятельно выбирать транспортные проекты, которые, на их взгляд, имеют наилучшую окупаемость и направлять заявки на реализацию данных проектов при минимальной конкуренции. Данная форма имеет ряд сложностей, связанных с возвратом средств частного сектора, потраченных на разработку предпроектной документации, и с согласованием существенных условий концессионного соглашения с публичным партнером, в частности  наличием/отсутствием капитального гранта, соотношением собственных и заемных средств, нормы доходности и другие.</p>

