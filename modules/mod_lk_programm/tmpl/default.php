						<p>В июне 2014 года был принят Федеральный закон № 172-ФЗ «О стратегическом планировании в Российской Федерации», который определил базовые положения процесса государственного стратегического планирования на всех уровнях власти. В развитие данного федерального закона ведется разработка Стратегии пространственного развития России, которая должна определить базовые инфраструктурные каркасы и приоритеты развития территорий в Российской Федерации. Также органами власти на разных уровнях продолжается разработка и утверждение отраслевых схем и планов территориального развития отдельных макрорегионов, территорий и населенных пунктов.</p>

						<p>Учитывая высокую чувствительность инфраструктурных проектов, реализуемых с использованием механизмов ГЧП, к платежеспособному спросу на соответствующие инфраструктурные услуги и как следствие к долгосрочным прогнозам и планам развития территорий в целом, актуальной является задача учета специфики таких проектов при разработке различных документов стратегического планирования, а также выработке путей решения накопившихся проблем в сфере планирования развития территорий, мешающих реализации долгосрочных ГЧП-проектов. Например, таких как завышение плановых показателей в отраслевых схемах, невыполнение регионами и муниципальными образованиями требований законодательства по принятию отдельных документов планирования. В рамках сессии планируется обсудить целесообразность и возможные подходы к учету специфики ГЧП в Стратегии пространственного развития России, проблемы увязки федеральных и региональных планов и стратегий, вопросы отраслевого и территориального планирования и другие.</p>

